const n1 = parseFloat(prompt("Digite o primeiro numero: "));
const n2 = parseFloat(prompt("Digite o segundo numero: "));

const soma = n1 + n2;
const sub = n1 - n2;
const prod = n1 * n2;
const div = n1 / n2;
const mod = n1 % n2;

alert("Soma: " + soma + "\nSubtração: " + sub + "\nProduto: " + prod
        + "\nDivisao: " + div + "\nResto: " + mod);